// 빗방울 배열 초기화 (비가 내리는 효과를 원하시면 주석을 해제하여 애니메이션을 넣을 수 있습니다. 현재는 정적 이미지 기준)
let rainDrops = [];

function setup() {
  // 생성한 캔버스를 변수에 담고 HTML 컨테이너에 바인딩합니다. (600x400 크기)
  let canvas = createCanvas(600, 400);
  canvas.parent('canvas-container');
  
  // 이미지와 동일한 위치에 빗방울 고정 데이터 생성
  rainDrops = [
    {x: 205, y: 65}, {x: 440, y: 75}, {x: 104, y: 110}, 
    {x: 340, y: 130}, {x: 365, y: 150}, {x: 255, y: 310},
    {x: 155, y: 260}, {x: 230, y: 280}, {x: 390, y: 300},
    {x: 415, y: 200}, {x: 490, y: 260}, {x: 525, y: 320}
  ];
}

function draw() {
  // 1. 바탕 배경 - 나무 벽면 색상
  background(160, 120, 90); 

  // 2. 나무 벽면 가로 줄무늬 패턴
  stroke(130, 90, 60); 
  strokeWeight(2);
  for (let y = 20; y < height; y += 30) {
    line(0, y, width, y);
  }

  // 3. 창문 안쪽 영역 (바깥 풍경이 보일 베이스)
  // 외곽 창틀이 덮어씌워질 것이므로 안쪽에 사각형으로 밤하늘을 먼저 깔아줍니다.
  noStroke();
  fill(15, 15, 28); // 밤하늘 색상
  rect(50, 50, 500, 300);

  // 4. 풍경 - 초승달
  fill(255, 215, 80); // 달의 밝은 노란색
  ellipse(450, 120, 50, 50);
  fill(15, 15, 28);   // 밤하늘 색상으로 파내어 초승달 만들기
  ellipse(438, 120, 43, 43);

  // 5. 풍경 - 뒤쪽 어두운 산
  noStroke();
  fill(20, 65, 35); // 어두운 그린
  triangle(150, 350, 300, 180, 520, 350); 

  // 6. 풍경 - 앞쪽 왼쪽 산
  fill(30, 125, 45); // 밝은 그린 1
  triangle(50, 350, 200, 220, 350, 350); 

  // 7. 풍경 - 앞쪽 오른쪽 산
  fill(25, 115, 40); // 중간 그린 2
  triangle(295, 350, 425, 250, 550, 350); 

  // 8. 풍경 - 내리는 빗방울 (불투명도가 있는 타원형)
  noStroke();
  fill(245, 245, 245, 120); // 흰색에 투명도 부여
  for (let i = 0; i < rainDrops.length; i++) {
    ellipse(rainDrops[i].x, rainDrops[i].y, 5, 12);
  }

  // 9. 겉에 덮어씌우는 메인 창틀 (두꺼운 회색 프레임 및 격자)
  stroke(45, 50, 55); 
  strokeWeight(14); 
  noFill();
  // 둥근 모서리를 가진 큰 창문 틀
  rect(50, 50, 500, 300, 12); 
  
  // 십자 격자 창살
  line(300, 50, 300, 350);  // 세로창살
  line(50, 200, 550, 200); // 가로창살
}
